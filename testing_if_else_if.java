public class testing_if_else_if {
    public static void main(String[] args){

        String str = "Covid";
        if ("Covid".equals(str)){
            System.out.println("You Have Covid");
        }
        else if ("Dengue".equals(str)){
            System.out.println("You Have Dengue");
        }
        else{
            System.out.println("You Are Healthy");
        }
        
    }
}
